#Faça um Programa que peça as 4 notas e calcule a média. Ao final do programa, mostre todas as notas e por fim, a média.
nota1 = float(input("Digite a primeira nota: "))
nota2 = float(input("Digite a segunda nota: "))
nota3 = float(input("Digite a terceira nota: "))
nota4 = float(input("Digite a quarta nota: "))

media = (nota1 + nota2 + nota3 + nota4) / 4
print("1ª Nota: ", nota1, "\n2ª Nota: ", nota2, "\n3ª Nota: ", nota3, "\n4ªNota: ", nota4, "\nMédia: ", media)