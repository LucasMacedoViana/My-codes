numero1 = int(input("Digite o primeiro número inteiro e positivo: "))
numero2 = int(input("Digite o segundo número inteiro e positivo: "))
soma = numero1 + numero2
multiplicacao = numero1 * numero2
media = (numero1 + numero2) / 2
quadrado = numero1 ** 2
cubo = numero2 ** 3

print("A soma entre os dois números é:", soma ,
      "\nO produtro entre os dois numeros é:", multiplicacao,
      "\nA média entre os dois números é:", media,
      "\nO primeiro número elevado ao quadroado é igual a:", quadrado,
      "\nO segundo número elevado ao cubo é igual a:", cubo)